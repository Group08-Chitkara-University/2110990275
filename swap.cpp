//swaping no. using 3rd variable

#include <iostream>
using namespace std;
int main(){

    int a = 9, b = 7, temp;
    cout<<"Before Swapping"<<endl;
    cout<<"a = "<<a<<endl;
    cout<<"b = " <<b<<endl;

    temp=a;//temp is 9
    a=b;//a is 7 
    b=temp;//b is 9



    cout<<"After swapping"<<endl;
    cout<<"a = "<<a<<endl;
    cout<<"b = "<<b<<endl;






}